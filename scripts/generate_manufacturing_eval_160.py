def main() -> int:
    rows = []
    for item in DATASET:
        domain_key, count, source_keywords, answer_keywords, type_cycle, queries = item[:6]
        expected_domain = item[6] if len(item) > 6 else domain_key
        for index, query in enumerate(queries[:count], start=1):
            rows.append(
                {
                    "id": f"{domain_key}_{index:03d}",
                    "query": query,
                    "expected_domain": expected_domain,
                    "expected_source_keywords": source_keywords,
                    "expected_answer_keywords": answer_keywords,
                    "question_type": type_cycle[(index - 1) % len(type_cycle)],
                }
            )

    if len(rows) != 160:
        raise RuntimeError(f"expected 160 rows, got {len(rows)}")

    output = Path("eval/manufacturing_rag_eval_160.jsonl")
    output.parent.mkdir(exist_ok=True)
    output.write_text(
        "\n".join(json.dumps(row, ensure_ascii=False) for row in rows) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {output} rows={len(rows)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
